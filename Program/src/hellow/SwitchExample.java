package hellow;

public class SwitchExample {
	public static void main(String[] args) {
		int n=19;
		switch(n) {
		case 1:
			System.out.println("1");
			break;
		case 10:
			System.out.println("10");
			break;
		case 11:
			System.out.println("11");
			break;
		case 15:
			System.out.println("15");
			break;
		default:
			System.out.println("0");
		}
	}

}
